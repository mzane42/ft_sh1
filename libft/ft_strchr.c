#include "libft.h"
#include <string.h>

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*p;

	i = 0;
	while (1)
	{
		if (s[i] == c)
		{
			p = &((char*)s)[i];
			return (p);
		}
		if (!s[i])
			break ;
		i++;
	}
	return (0);
}
